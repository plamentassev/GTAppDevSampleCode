package edu.gatech.seclass.project3;

public class Grade {
	private String gtid;
	private String assignementName;
	private int assignementGrade;
	public String getGtid() {
		return gtid;
	}
	public void setGtid(String gtid) {
		this.gtid = gtid;
	}
	public String getAssignementName() {
		return assignementName;
	}
	public void setAssignementName(String assignementName) {
		this.assignementName = assignementName;
	}
	public int getAssignementGrade() {
		return assignementGrade;
	}
	public void setAssignementGrade(int assignementGrade) {
		this.assignementGrade = assignementGrade;
	}
}
